// Helper: Format Tank ID (e.g. F59 -> F-59, AOK -> A-OK)
function formatTankId(rawId) {
  if (!rawId) return "";
  const match = rawId.match(/^([A-Z])(.*)$/);
  if (match) {
    return `${match[1]}-${match[2]}`;
  }
  return rawId;
}

// Main Import Function
async function importData() {
  const lines = rawData.trim().split("\n");
  let importCount = 0;
  
  const batchArray = [];
  let currentBatch = db.batch();
  let opCount = 0;

  for (let idx = 0; idx < lines.length; idx++) {
    const cols = lines[idx].split("\t");
    
    const rawId = cols[0]?.trim();
    if (!rawId || rawId.length === 0) continue; // Skip blank lines
    
    // Ignore lines that are just whitespace or don't look like an ID
    if (rawId.length > 5) continue;

    const id = formatTankId(rawId);
    if (!id || id.length === 0 || id.includes("/")) continue; // Skip if ID is still blank after formatting or looks like a date
    
    // As per user request: EVERYTHING defaults to Empty and Warehouse upon initial load
    const status = "空";
    const locRaw = "倉庫";
    
    // skipping staff (cols[3])
    const expireRaw = cols[4]?.trim() || "";
    const noteDefect = cols[5]?.trim() || "";
    const noteNormal = cols[6]?.trim() || "";
    const typeRaw = cols[8]?.trim() || "スチール 10L";

    // Build the data object
    const tankData = {
      id: id,
      tankId: id,
      prefix: id.charAt(0) || "A",
      status: status,
      location: locRaw,
      type: typeRaw,
      maintenanceDate: expireRaw,
      nextMaintenanceDate: expireRaw, // Using the same text for now
      tags: [],
      notes: noteNormal || noteDefect || ""
    };
    
    // Default system tags (used in settings)
    const defaults = ["A", "B", "C", "D", "E", "F"];
    if (!defaults.includes(tankData.prefix)) {
      tankData.prefix = tankData.prefix; // keep as is
    }

    const docRef = db.collection("tanks").doc(id);
    currentBatch.set(docRef, tankData);
    opCount++;

    // Firestore batch limit is 500
    if (opCount >= 450) {
      batchArray.push(currentBatch);
      currentBatch = db.batch();
      opCount = 0;
    }
    
    importCount++;
  }

  // push the last batch
  if (opCount > 0) {
    batchArray.push(currentBatch);
  }

  console.log(`Ready to upload ${importCount} tanks in ${batchArray.length} batches.`);
  
  // Execute batches
  for (let i = 0; i < batchArray.length; i++) {
    await batchArray[i].commit();
    console.log(`Batch ${i + 1} committed.`);
  }
  
  console.log("Migration complete!");
}

importData().catch(console.error);
